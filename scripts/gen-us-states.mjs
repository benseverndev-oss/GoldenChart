import { writeFileSync } from 'node:fs';
import { geoAlbersUsa, geoPath } from 'd3-geo';
import { feature } from 'topojson-client';
import states10m from 'us-atlas/states-10m.json' with { type: 'json' }; // Node 22: `with`, not `assert`

// Census state FIPS id -> USPS 2-letter code (50 states + DC).
const FIPS_TO_USPS = {
  '01': 'AL', '02': 'AK', '04': 'AZ', '05': 'AR', '06': 'CA', '08': 'CO', '09': 'CT', '10': 'DE',
  '11': 'DC', '12': 'FL', '13': 'GA', '15': 'HI', '16': 'ID', '17': 'IL', '18': 'IN', '19': 'IA',
  '20': 'KS', '21': 'KY', '22': 'LA', '23': 'ME', '24': 'MD', '25': 'MA', '26': 'MI', '27': 'MN',
  '28': 'MS', '29': 'MO', '30': 'MT', '31': 'NE', '32': 'NV', '33': 'NH', '34': 'NJ', '35': 'NM',
  '36': 'NY', '37': 'NC', '38': 'ND', '39': 'OH', '40': 'OK', '41': 'OR', '42': 'PA', '44': 'RI',
  '45': 'SC', '46': 'SD', '47': 'TN', '48': 'TX', '49': 'UT', '50': 'VT', '51': 'VA', '53': 'WA',
  '54': 'WV', '55': 'WI', '56': 'WY',
};

const VIEWBOX = [960, 600];
const fc = feature(states10m, states10m.objects.states);
const projection = geoAlbersUsa().fitSize(VIEWBOX, fc);
const path = geoPath(projection);

const out = {};
for (const f of fc.features) {
  const code = FIPS_TO_USPS[String(f.id).padStart(2, '0')];
  const d = path(f);
  if (code && d) out[code] = d;
}
if (Object.keys(out).length < 50) throw new Error(`only ${Object.keys(out).length} states projected`);

writeFileSync(
  'src/assets/usStates.ts',
  `// Generated by scripts/gen-us-states.mjs — do not edit by hand.\n` +
    `// Pre-projected (Albers-USA) US state outlines in a ${VIEWBOX[0]}x${VIEWBOX[1]} viewBox.\n` +
    `export const US_STATES_VIEWBOX = { width: ${VIEWBOX[0]}, height: ${VIEWBOX[1]} } as const;\n` +
    `export const US_STATES_GEOMETRY: Record<string, string> = ${JSON.stringify(out)};\n`,
);
console.log(`wrote ${Object.keys(out).length} states`);
